public class GasMileage {

        public static int milesDrive(int milesDriven) {
            milesDriven = milesDriven;
            return milesDriven;
        }

        public static int gallonUsed(int gallonUsed) {
//            gallonsUsed = gallonsUsed;
            return gallonUsed;
        }

        public static double milesPerGallon(int milesDriven, int gallonUsed) {
            double milesPerGallon = (double) milesDriven / gallonUsed;
            return milesPerGallon;
        }

    }
